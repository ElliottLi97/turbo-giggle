const { Schema ,model } = require('mongoose');

const doctorsSchema = new Schema({
    id: {
        type: Number,
        required: true,
        unique: true
    },
    name: {
        type: String,
        required: true
    },
    gender: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    }
});

const doctors = model('doctors', doctorsSchema);

module.exports = doctors;